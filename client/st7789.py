"""
Minimal ST7789 driver for MicroPython (ESP32-S3).

Supports fill, rectangle, single-character and string drawing using a
built-in 8×8 monospace font.  No full framebuffer required — each
primitive writes directly to the display over SPI.

Designed for the Waveshare ESP32-S3 1.47" LCD (172×320, ST7789V2).
"""

import machine
import time
from struct import pack

# ST7789 commands
_NOP       = 0x00
_SWRESET   = 0x01
_SLPOUT    = 0x11
_NORON     = 0x13
_INVON     = 0x21
_DISPON    = 0x29
_CASET     = 0x2A
_RASET     = 0x2B
_RAMWR     = 0x2C
_MADCTL    = 0x36
_COLMOD    = 0x3A

# MADCTL flags
_MADCTL_MY  = 0x80
_MADCTL_MX  = 0x40
_MADCTL_MV  = 0x20
_MADCTL_RGB = 0x00

# Rotation presets  (MADCTL value, width, height, x-offset, y-offset)
_ROTATIONS = {
    0: (_MADCTL_MX | _MADCTL_RGB,              172, 320, 34, 0),
    1: (_MADCTL_MV | _MADCTL_MX | _MADCTL_RGB, 320, 172,  0, 34),
    2: (_MADCTL_MY | _MADCTL_RGB,              172, 320, 34, 0),
    3: (_MADCTL_MV | _MADCTL_MY | _MADCTL_RGB, 320, 172,  0, 34),
}

class ST7789:
    """Drive an ST7789 LCD over hardware SPI."""

    def __init__(self, spi, dc, cs, rst, bl, rotation=0):
        self.spi = spi
        self.dc  = dc
        self.cs  = cs
        self.rst = rst
        self.bl  = bl

        madctl, self.width, self.height, self._xoff, self._yoff = _ROTATIONS[rotation]
        self._madctl = madctl

        self._buf1 = bytearray(1)
        self._buf2 = bytearray(2)

        self.reset()
        self._init_display()
        self.backlight(True)

    # --- low-level ------------------------------------------------------------

    def reset(self):
        self.rst.value(1); time.sleep_ms(50)
        self.rst.value(0); time.sleep_ms(50)
        self.rst.value(1); time.sleep_ms(150)

    def _write_cmd(self, cmd):
        self.dc.value(0)
        self.cs.value(0)
        self._buf1[0] = cmd
        self.spi.write(self._buf1)
        self.cs.value(1)

    def _write_data(self, data):
        self.dc.value(1)
        self.cs.value(0)
        self.spi.write(data)
        self.cs.value(1)

    def _init_display(self):
        for cmd, data, delay in (
            (_SWRESET, None,           150),
            (_SLPOUT,  None,           255),
            (_COLMOD,  b'\x55',         10),   # 16-bit colour
            (_MADCTL,  bytes([self._madctl]), 0),
            (_INVON,   None,            10),
            (_NORON,   None,            10),
            (_DISPON,  None,           100),
        ):
            self._write_cmd(cmd)
            if data:
                self._write_data(data)
            if delay:
                time.sleep_ms(delay)

    def backlight(self, on):
        self.bl.value(1 if on else 0)

    # --- drawing primitives ---------------------------------------------------

    def _set_window(self, x0, y0, x1, y1):
        x0 += self._xoff; x1 += self._xoff
        y0 += self._yoff; y1 += self._yoff
        self._write_cmd(_CASET)
        self._write_data(pack('>HH', x0, x1))
        self._write_cmd(_RASET)
        self._write_data(pack('>HH', y0, y1))
        self._write_cmd(_RAMWR)

    def fill(self, color):
        """Fill the entire screen with *color* (RGB565)."""
        self._set_window(0, 0, self.width - 1, self.height - 1)
        # send in chunks to avoid huge alloc
        chunk_lines = 8
        line_bytes = self.width * 2
        buf = bytes([color >> 8, color & 0xFF]) * (self.width * chunk_lines)
        lines_left = self.height
        while lines_left > 0:
            n = min(chunk_lines, lines_left)
            self._write_data(buf[:n * line_bytes])
            lines_left -= n

    def fill_rect(self, x, y, w, h, color):
        """Fill a rectangle at (x, y) of size w×h with *color*."""
        if x + w > self.width:
            w = self.width - x
        if y + h > self.height:
            h = self.height - y
        if w <= 0 or h <= 0:
            return
        self._set_window(x, y, x + w - 1, y + h - 1)
        chunk_lines = 8
        line_bytes = w * 2
        buf = bytes([color >> 8, color & 0xFF]) * (w * chunk_lines)
        lines_left = h
        while lines_left > 0:
            n = min(chunk_lines, lines_left)
            self._write_data(buf[:n * line_bytes])
            lines_left -= n

    def pixel(self, x, y, color):
        self._set_window(x, y, x, y)
        self._write_data(pack('>H', color))

    # --- text -----------------------------------------------------------------

    def draw_char(self, ch, x, y, fg, bg=None, scale=1):
        """Draw a single character using the built-in 8×8 font."""
        idx = min(max(ord(ch) - 32, 0), len(_FONT) - 1)
        glyph = _FONT[idx]
        char_w = 8 * scale
        char_h = 8 * scale
        if bg is not None:
            self._set_window(x, y, x + char_w - 1, y + char_h - 1)
            buf = bytearray(char_w * char_h * 2)
            pos = 0
            for row in range(8):
                bits = glyph[row]
                for _ in range(scale):
                    for col in range(8):
                        c = fg if bits & (1 << col) else bg # c = fg if bits & (0x80 >> col) else bg
                        for __ in range(scale):
                            buf[pos]     = c >> 8
                            buf[pos + 1] = c & 0xFF
                            pos += 2
            self._write_data(buf)
        else:
            for row in range(8):
                bits = glyph[row]
                for col in range(8):
                    if bits & (1 << col): #  if bits & (0x80 >> col):
                        if scale == 1:
                            self.pixel(x + col, y + row, fg)
                        else:
                            self.fill_rect(x + col * scale, y + row * scale, scale, scale, fg)

    def text(self, s, x, y, fg, bg=None, scale=1):
        """Draw a string. Returns the x position after the last character."""
        for ch in s:
            if x + 8 * scale > self.width:
                break
            self.draw_char(ch, x, y, fg, bg, scale)
            x += 8 * scale
        return x


